type 可异步函<入, 出> = (入: 入) => Promise<出> | 出;
type 可异步函列 = (函<any, any> | 可异步函<any, any>)[];

function _续函<入, 出>(...函列: 函续列): 函<入, 出> {
  return (入: 入) => 函列.reduce((值, 函) => 函(值), 入 as any);
}
function _异步续函<入, 出>(...异步函列: 可异步函列): 可异步函<入, 出> {
  return async (入: 入) => {
    let 出: any = await 入;
    for await (const 异步函 of 异步函列) {
      出 = await 异步函(出);
    }
    return 出 as 出;
  };
}

